/**
 * @brief  根据降序 ADC/mV 表查表插值，得到 dBm
 *
 * @param ad_table        ADC/mV 输入表，要求降序排列
 * @param dbm_x10_table   dBm*10 输出表，长度必须和 ad_table 一致
 * @param len             表长度
 * @param value           当前 ADC/mV 值
 * @param offset_db       补偿偏移，单位 dB，例如 +1.5f
 * @param out_dbm         输出 dBm，例如 -23.4f
 *
 * @return true 成功，false 参数错误
 */
bool Calibration_AdcToDbm(const uint16_t ad_table[],
                          const int16_t dbm_x10_table[],
                          uint16_t len,
                          uint16_t value,
                          float offset_db,
                          float* out_dbm)
{
    if (ad_table == nullptr || dbm_x10_table == nullptr || out_dbm == nullptr) {
        return false;
    }

    if (len < 2) {
        return false;
    }

    // 两端夹紧：表是降序，ad_table[0] 最大，ad_table[len - 1] 最小
    if (value >= ad_table[0]) {
        *out_dbm = ((float)dbm_x10_table[0] * 0.1f) + offset_db;
        return true;
    }

    if (value <= ad_table[len - 1]) {
        *out_dbm = ((float)dbm_x10_table[len - 1] * 0.1f) + offset_db;
        return true;
    }

    // 找到区间：ad_table[i] >= value >= ad_table[i + 1]
    for (uint16_t i = 0; i < len - 1; ++i) {
        uint16_t x0 = ad_table[i];
        uint16_t x1 = ad_table[i + 1];

        if (value <= x0 && value >= x1) {
            float y0 = (float)dbm_x10_table[i] * 0.1f;
            float y1 = (float)dbm_x10_table[i + 1] * 0.1f;

            float dx = (float)x1 - (float)x0;
            float dv = (float)value - (float)x0;

            if (dx == 0.0f) {
                *out_dbm = y0 + offset_db;
                return true;
            }

            // 线性插值：y = y0 + (y1 - y0) * (value - x0) / (x1 - x0)
            *out_dbm = y0 + (y1 - y0) * dv / dx + offset_db;
            return true;
        }
    }

    return false;
}

bool Calibration_AdcToUw(const uint16_t ad_table[],
                         const uint32_t uw_table[],
                         uint16_t len,
                         uint16_t value,
                         int32_t offset_uw,
                         uint32_t* out_uw)
{
    if (ad_table == nullptr || uw_table == nullptr || out_uw == nullptr) {
        return false;
    }

    if (len < 2) {
        return false;
    }

    uint32_t power_uw = 0;

    // 两端夹紧：表是降序
    if (value >= ad_table[0]) {
        power_uw = uw_table[0];
    } else if (value <= ad_table[len - 1]) {
        power_uw = uw_table[len - 1];
    } else {
        bool found = false;

        for (uint16_t i = 0; i < len - 1; ++i) {
            uint16_t x0 = ad_table[i];
            uint16_t x1 = ad_table[i + 1];

            if (value <= x0 && value >= x1) {
                uint32_t y0 = uw_table[i];
                uint32_t y1 = uw_table[i + 1];

                int32_t dx = (int32_t)x1 - (int32_t)x0;
                int32_t dv = (int32_t)value - (int32_t)x0;

                if (dx == 0) {
                    power_uw = y0;
                } else {
                    int64_t dy = (int64_t)y1 - (int64_t)y0;
                    int64_t interp = (int64_t)y0 + dy * dv / dx;

                    if (interp < 0) {
                        interp = 0;
                    }

                    power_uw = (uint32_t)interp;
                }

                found = true;
                break;
            }
        }

        if (!found) {
            return false;
        }
    }

    // offset_uw 是线性功率偏移，直接相加
    int64_t corrected = (int64_t)power_uw + (int64_t)offset_uw;

    if (corrected <= 0) {
        *out_uw = 0;
        return true;
    }

    if (corrected > 0xFFFFFFFFLL) {
        *out_uw = 0xFFFFFFFFu;
        return true;
    }

    *out_uw = (uint32_t)corrected;
    return true;
}
